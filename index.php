      const nextbtn = document.querySelector(".next");
      const prevbtn = document.querySelector(".prev");
      const ul = document.querySelector(".dots");
      const items = Array.from(document.querySelectorAll(".slide"));
      const slideCount = items.length;
      let currentIndex = 0;

      nextbtn.addEventListener("click", nextSlide);
      prevbtn.addEventListener("click", prevSlide);

      for (let i = 1; i <= slideCount; i++) {
        const li = document.createElement("li");
        li.setAttribute("data_idex", i);
        ul.appendChild(li);
      }
      function nextSlide() {
        if (nextbtn.classList.contains("disabled")) {
          return;
        } else {
          currentIndex++;
          check();
        }
      }

      function prevSlide() {
        if (prevbtn.classList.contains("disabled")) {
          return;
        } else {
          currentIndex--;
          check();
        }
      }

      function check() {
        items.forEach((item) => {
          item.classList.remove("active");
        });
        Array.from(ul.children).forEach((item) => {
          item.classList.remove("active");
        });

        items[currentIndex].classList.add("active");
        ul.children[currentIndex].classList.add("active");

        if (currentIndex == 0) {
          prevbtn.classList.add("disabled");
        } else {
          prevbtn.classList.remove("disabled");
        }

        if (currentIndex == slideCount - 1) {
          nextbtn.classList.add("disabled");
        } else {
          nextbtn.classList.remove("disabled");
        }
      }
      Array.from(ul.children).forEach((li) => {
        li.addEventListener("click", () => {
          let index = li.getAttribute("data_idex") - 1;
          currentIndex = index;
          check();
        });
      });
      check();
 
    </script>

