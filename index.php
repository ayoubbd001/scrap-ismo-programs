      const nav = document.querySelector("nav");

      window.addEventListener("scroll", () => {
        let size = window.pageXOffset || document.documentElement.scrollTop;
        if (size > 0) {
          nav.style.top = "0%";
        } else if (size == 0) {
          nav.style.top = "25px";
